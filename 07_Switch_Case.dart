void main()
{
  //Switch case statements : Application for only 'int' and 'string'
  
  String grade = 'A';
  
  switch (grade)
  {
    case 'A':
      print("Exellent grade of A");
      break;
      
    case 'B':
      print("very good!");
      break;
      
    case 'C':
      print("good!");
      break;
      
    default:
      print("YOU HAVE FAILED ");
      
      
      
      
  }
}